def triple(number):
    """
    Triple a number.

    :param number: an integer or a float
    :precondition: number must be an integer or a float
    :postcondition: the number is tripled correctly
    :return: a number which is three times the original number

    >>> triple(3)
    9
    >>> triple(-2.3)
    -6.9
    """

    return number * 3
